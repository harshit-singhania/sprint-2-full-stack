package com.example.usedcars.model;

/**
 * Seller-declared physical condition of the car.
 * Used when displaying and comparing listings.
 */
public enum CarCondition {
    EXCELLENT,   // Like new, no visible wear
    GOOD,        // Minor wear, fully functional
    FAIR,        // Noticeable wear, may need minor repairs
    POOR         // Significant wear or mechanical issues
}
